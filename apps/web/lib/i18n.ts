export const LOCALE_COOKIE = "mp_locale"

export const appLocales = ["en", "zhtw", "zhcn"] as const

export type AppLocale = (typeof appLocales)[number]

const localeAliases: Record<string, AppLocale> = {
  en: "en",
  "en-us": "en",
  "en_us": "en",
  zhtw: "zhtw",
  "zh-tw": "zhtw",
  "zh_tw": "zhtw",
  "zh-hant": "zhtw",
  "zh_hant": "zhtw",
  zhcn: "zhcn",
  "zh-cn": "zhcn",
  "zh_cn": "zhcn",
  "zh-hans": "zhcn",
  "zh_hans": "zhcn",
}

export function normalizeLocale(value: string | null | undefined): AppLocale | null {
  if (!value) return null
  return localeAliases[value.toLowerCase()] ?? null
}

export function localeLabel(locale: AppLocale): string {
  return dictionaries.en.locale.names[locale]
}

export function htmlLang(locale: AppLocale): string {
  if (locale === "zhtw") return "zh-Hant"
  if (locale === "zhcn") return "zh-Hans"
  return "en"
}

export function intlLocale(locale: AppLocale = "en"): string {
  if (locale === "zhtw") return "zh-TW"
  if (locale === "zhcn") return "zh-CN"
  return "en-US"
}

